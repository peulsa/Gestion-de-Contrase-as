package com.example.tareahenrik;

public class Clave {
    private String userName;
    private String clave;

    // Constructor
    public Clave(String userName, String clave) {
        this.userName = userName;
        this.clave = clave;
    }

    // Getters
    public String getUserName() {
        return userName;
    }

    public String getClave() {
        return clave;
    }
}



